          <div class="short-histo text-justify">
             <p>Berawal dari kurangnya jumlah insinyur indonesia pada tahun 1983 menimbulkan gagasan pendirian pendidikan tinggi teknik oleh Persatuan Insinyur Indonesia (PII). Untuk mendukung gagasan tersebut maka tanggal 24 Februari 1984 didirikanlah Yayasan Pengembangan Teknologi Indonesia (YPTI) dengan akta No.167 Notaris Ny. Sumardilah Oriana Roosdilan, SH. <br><br>

             Kongres Nasional ke XI PII di Palembang pada tanggal 17-19 Mei 1984 menugaskan kepada pengurus baru Pil untuk mendirikan perguruan tinggi teknik dan ditindaklanjuti dengan diterbitkannya surat keputusan dari YPTI No.01/Kept-YPTI/1984 pada tanggal 2 Juni 1984 tentang pendirian Institut Teknologi Indonesia (ITI) terdiri dari 3 (Tiga) Fakultas dengan 9 (Sembilan) Jurusan. Ijin prinsip dari Depdikbud dikeluarkan tangga; 9 Agustus 1984 No.1068/D/0/1984, disusul dengan ijin operasional dari Kopertis wilayah III tanggal 14 Agustus 1984 No.2/Kopt.IIIA/I II/84. Kuliah pertama dimulai pada tanggal 1 Oktober 1984 dengan jumlah mahasiswa sebanyak 900 orang dan tanggal bersejarah ini dijadikan sebagai Hari Lahir (Dies Natalis) ITI.<br><br>

             Tahun 2006, dalam menyikapi perkembangan yang terjadi dilakukan penguatan pengelolaan melalui pembaharuan struktur organisasi yayasan sesuai akta Notaris Nomor 11 yang ditanda tangani Faisal Abu Yusuf, SH, tangga; 14 Desember 2006.<br><br>

             Tahun 2007-2008 dilakukan reorganisasi dan restrukturisasi manajemen, reorientasi kurikulum, serta optimalisasi SDM baik Ddosen maupun karyawan. Perubahan mendasar ini telah merubah warna dan corak pendidikan ITI menjadi “The Technology Based Entrepreneur University”.<br><br>

             Saat ini, YPTI dipimpin oleh Prof. Dr. Ing. B.J. Habibie, sebagai Ketua Pembina, dan Ir. Aburizal Bakrie, sebagai Ketua Harian Pembina. Sedangkan ITI, dengan Visi dan Misi baru, dipimpin oleh Dr.Ir. Isnuwardiantoro sebagai Rektor ITI.</p>
