         <div class="komitmen text-justify">
            <h5>KOMITMEN</h5><br>
            <P> Berdasarkan amanah Kongres Persatuan Insinyur Indonesia (PII), untuk mengatasi masalah kelangkaan insinyur di Indonesia, Yayasan Pengembangan Teknologi Indonesia (YPTI) pada tahun 1984 mendirikan Institut Teknologi  Indonesia (ITI).<br><br>

            ITI memiliki komitmen untuk berperan aktif dalam mengangkat Daya Saing dan Martabat Bangsa Indonesia, melalui system pendidikan khususnya dalam bidang Pendidikan Tinggi Keteknikan / Teknologi yang Efisien – Berkualitas agar terjangkau oleh sebanyak mungkin Anak Bangsa untuk melanjutkan pendidikan.<br><br>

            Seluruh Program (Kurikuler, ko-kurikuler, ekstrakurikuler) dikemas secara kreatif untuk dapat mereduksi biaya kuliah dengan tetap menjaga kualitas yang diharapkan akan menjadi SOLUSI atas keluhan masyarakat Indonesia tentang Pendidikan Berkualitas Tapi Mahal, atau Pendidikan yang Murah Tapi Kurang Memperhatikan Kualitas.<br><br>

            Pendidikan di ITI dirancang untuk menghasilkan lulusan yang tidak hanya memiliki pengetahuan dan keterampilan tetapi juga sikap mental untuk dapat menggali / meraih / menciptakan peluang, sehingga lulusan ITI tidak hanya menjadi objek masa depan, tetapi menjadi subyek guna MENCIPTAKAN MASA DEPAN yang lebih baik.<br><br>

            Oleh karena itu, program akademik ITI mengarah pada Technology Based Entrepreneur University.</P>   
