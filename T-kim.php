              <li>TEKNIK KIMIA adalah suatu bidang ilmu yang mempelajari cara-cara disiplin ilmu yang mempelajari cara-cara menerapkan hukum-hukum kimia dan fisika kedalam perencanaan, pembangunan, pelaksanaan operasi dari pabrik-pabrik yang mengolah bahan baku menjadi berbagai produk secara ekonomis. Atau secara lengkapnya seorang mahasiswa Teknik Kimia dipersiapkan untuk dapat merencanakan dan merancang alat-alat proses, mengoperasikan, mengendalikan dan memelihara pabrik / industri, mengkontruksi pendirian suatu pabrik (dalam batas batas tertentu) mengadakan penelitian dan pengembangan proses, dan merencanakan serta memanajemen penjualan dan pelayanan.</li><br>
              <li> Institut Teknologi Indonesia yang didirikan sejak tahun 1984 telah menghasilkan lebih dari 1000 orang sarjana Teknik Kimia, yang telah bekerja di berbagai bidang. Program Studi Teknik Kimia ITI (PSTK-ITI) telah memperoleh status terakreditasi sejak tahun 1989, dengan nilai akreditasi “B” oleh Badan Akreditasi Nasional Perguruan Tinggi (BAN-PT). Saat ini Akreditasi “B” dengan  No. 441/SK/BAN-PT/Akred/S/XI/2014 berlaku sejak 08 November 2014 sampai dengan 07 November 2019.</li><br>
              <li>Kurikulum PSTK-ITI terdiri dari matakuliah wajib (131 sks) dan matakuliah peminatan (10 sks). Matakuliah Wajib terdiri dari kelompok Ilmu Dasar Umum (13 SKS), Dasar Keahlian (Basic Science) (25 SKS), Ilmu Teknik (Engineering Science)  (79 SKS),  Tugas Mandiri Terbimbing (Kerja Praktek, Penelitian, Seminar Penelitian,  Tugas Akhir Pra-rancangan Pabrik Kimia (Sripsi), Sidang Tugas Akhir) (14 SKS). Dan matakuliah peminatan ada 3 bidang peminatanyaitu Teknologi dan Rekayasa Material, Teknologi Lingkungan dan Produk Hayati, Teknologi Konversi Energi.</li><br>
              <li> Lulusan Teknik Kimia dapat bekerja di berbagai macam jenis industri kimia seperti misalnya : industri perminyakan dan gas alam, pertambangan batu bara, industri semen, industri pupuk, , industri polimer, industri pulp dan kertas, industri petrokimia, industri plastik, industri farmasi, industri makanan dan minuman, industri keramik, industri gelas dan industri pengolahan limbah. Selain di Industri  kimia, seorang sarjana Teknik Kimia dapat juga bekerja di bidang penelitian, pendidikan, pemasaran, manufaktur peralatan industri jasa konsultan dan kewirausahaan. Beberapa perusahaan yang merekrut lulusan sarjana teknik kimia ITI antara lain : PT. BASF Indonesia, PT. Pindo Deli Pulp & Paper Mills, Pertamina, PT. Bayer Indonesia, PT. Surveyor Indonesia, PT. National Gobel, PT. Japan Gas Contructions, PT. Tripatra, PT. Mayora, PT. Yupi Jelly Gum, PT. Chandra Asri, PT. TIFICO, PT. Indah Kiat, PT. Cevron, PT. Lautan Luas</li><br>
