              <p class="news-ypti text-justify">
              Institut Teknologi Indonesia (ITI) didirikan pada tahun 1983 oleh Yayasan Pengembangan Teknologi Indonesia (YPTI) dibawah pimpinan Prof.Dr. Ing. B.J. Habibie selaku Ketua Dewan Pembina YPTI dan Menteri Ristek pada waktu itu, atas dasar gagasan Persatuan Insinyur Indonesia (PII) pada Kongres PII Palembang tahun 1983 untuk meningkatkan tenaga insinyur di Indonesia. <br><br>

              Dasar pendirian ITI bertitik tolak pada pemikiran bahwa “Teknologi adalah milik semua Bangsa” dan bahwa Sumber Daya Manusia, tenaga ahli yang profesional adalah mutlak diperlukan dan mendesak untuk mengejar ketinggalan dari bangsa lain yang telah maju.<br><br>

              Menyadari akan kebutuhan Sarjana Teknik yang diperlukan dalam Pembangunan Nasional serta keterbatasan Lembaga Perguruan Tinggi dalam menghasilkan Sarjana Teknik pada waktu itu, YPTI didukung penuh oleh PII mendirikan ITI untuk membantu mengisi kekurangan tenaga Sarjana Teknik tersebut.<br><br>

              Institut Teknologi Indonesia ditetapkan pendiriannya melalui Surat Keputusan Yayasan No. 01/Kept-YPTI/84 Tanggal 02 Juni 1984 dan telah memulai kegiatan perkuliahannya tanggal 01 oktober 1984.<br><br>

              Dalam perkembanganya dan menyikapi perubahan serta keinginan untuk tetap eksis, maka pada tahun 2006 dilakukan penguatan pengelolaan melalui pembaharuan Struktur Organisasi Yayasan sesuai Akte Notaris yang ditanda tangani Faisal Abu Yusuf SH Nomor 11 tanggal 14 Desember Tahun 2006.<br><br>

              <span>Pada saat ini YPTI dipimpin oleh Prof. Dr. Ing. B.J. Habibie sebagai Ketua Pembina dan Ir. Abu Rizal Bakrie sebagai Ketua Harian.</span>
