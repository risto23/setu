          <h4 style="margin:80px 0 20px 0;">Selayang Pandang Teknik Industri</h4>
          <p class="text-justify">Teknik Industri adalah suatu bidang keilmuan yang mempelajari bagaimana merancang, mengatur dan mengaplikasikan semua faktor-faktor seperti manusia, mesin, metode, material, lingkungan dan analisis keuangan serta kajian manajerial menjadi suatu sistem dalam lingkup yang berhubungan dengan fungsi industri seperti penelitian dasar, penelitian operasional, pengembangan terhadap suatu produk baru melalui rekayasa industri, desain produk, perancangan sistem kerja dan ergonomi, perawatan mesin, pengendalian kualitas dan mutu, otomasi sistem produksi hingga pada analisa kelayakan pabrik yang mencakup pula kajian manajemen secara komprehensif dan aplikatif.<br><br>

          Teknik Industri Institut Teknologi Indonesia (TI – ITI) yang didirikan tahun 1984 saat ini memiliki 2 peminatan utama yaitu Sistem Manufaktur dan Sistem Manajemen  yang mana kurikulum dan arahan dari para dosen membuat alumninya mampu melakukan analisis komprehensif terhadap berbagai permasalahan di dunia industri manufaktur maupun jasa dan perdagangan serta mampu memberikan kajian yang bersifat adaptif, aplikatif serta memiliki nilai tambah sesuai dengan Visi dan Misi Program Studi Teknik Industri  – ITI yang merupakan derivatif Visi dan Misi Institut Teknologi Indonesia yaitu Enterpreneur Based on Technology sehingga para alumni Teknik Industri – ITI dapat melakukan analisis dari perspektif manufaktur dan manajemen secara komprehensif serta inovatif sesuai dengan tantangan dan peluang yang terjadi pada era globalisasi.<br><br>

          Pencapaian signifikan dan krusial oleh TI – ITI selama ini antara lain diperolehnya Nilai Akreditasi “B” oleh BAN-PT tahun 2016-2021; telah lebih dari 1000 alumni TI – ITI yang berkontribusi di dunia industri dan perusahaan serta instansi Pemerintah dengan berbagai strata jenjang karier, banyak alumni TI – ITI yang berhasil dalam jenjang pendidikan di S2 dan S3 baik di dalam maupun luar negeri, dosennya ada yang telah berhasil menjadi Dosen Berprestasi Peringkat 1 tingkat Kopertis Wilayah III Jakarta, 7 orang dosen tetap Teknik Industri – ITI telah memperoleh Sertifikasi Dosen dari Kementerian Pendidikan Nasional.<br><br>

          Berhasilnya mahasiswa Teknik Industri – ITI meraih peringkat 5 Lomba Kewirausahaan Tingkat Nasional, dicapainya peringkat 4 Lomba Olimpiade MIPA tingkat Nasional bidang Fisika oleh mahasiswi Teknik Industri – ITI,  kesuksesan meraih Hibah Bersaing oleh dosen tetap TI – ITI pada Kompetisi Hibah untuk Kewirausahaan tingkat Nasional, adanya Kelompok Ilmiah Mahasiswa Dosen (KIMD) dan Kelompok Enterpreneur Mahasiswa (KEMA) yang merupakan wahana ilmiah menuju kompetisi tingkat Nasional, menjadi anggota aktif dari Persatuan Insinyur Indonesia (PII) pada Badan Koordinasi Studi Teknik Industri (BKSTI).  Banyaknya paper yang dibuat oleh para dosen tetap TI – ITI yang telah dipublikasikan pada Jurnal Internasional berindex SCOPUS, Thompson-Readers, IAEA dan Copernicus juga IEEE maupun Nasional ter-Akreditasi; hal tersebut merupakan langkah TI – ITI untuk continuos improvement sehingga secara berkesinambungan dapat mewujudkan Visi dan Misi Program Studi serta jalinan mata rantai dengan dunia industri serta masyarakat.</p>
